group_1 = ["Иванов", "Петров", "Сидоров", "Кузнецов", "Соколов",
           "Михайлов", "Федоров", "Николаев", "Алексеев", "Дмитриев"]

group_2 = ["Смирнов", "Васильев", "Попов", "Козлов", "Новиков",
           "Морозов", "Волков", "Соловьев", "Лебедев", "Зайцев"]

print("a) Создание спортивной команды")

team = group_1[:5] + group_2[:5]
team_tuple = tuple(team)

print(f"Группа 1 (10 студентов): {group_1}")
print(f"Группа 2 (10 студентов): {group_2}")
print(f"Спортивная команда (кортеж): {team_tuple}")

print(f"c) Длина кортежа: {len(team_tuple)}")

sorted_team = tuple(sorted(team_tuple))
print(f"d) Отсортированная команда по алфавиту: {sorted_team}")

count_ivanov = team_tuple.count("Иванов")

print(f"e) Проверка наличия студента '{"Иванов"}':")
if "Иванов" in team_tuple:
    print(f"   Студент '{"Иванов"}' входит в команду")
    print(f"   Фамилия '{"Иванов"}' встречается {count_ivanov} раз(а)")
else:
    print(f"   Студент '{"Иванов"}' не входит в команду")
